  CREATE OR REPLACE FORCE EDITIONABLE VIEW "GIS_ONC"."AREA_LIGHT_VW" ("OGGY_H", "OGGX_H", "OWNER1_ID", "G3E_FNO", "FEATURE_STATE_C", "LONGITUDE", "G3E_FID", "LOCATION", "LATITUDE", "STRUCTURE_ID", "G3E_GEOMETRY", "VINTAGE_YR", "LUMIN_STYL_C", "LAMP_TYPE_C", "WATT_Q") AS 
  SELECT CN.OGGY_H,
  CN.OGGX_H,
  CN.OWNER1_ID,
  CN.G3E_FNO,
  CN.FEATURE_STATE_C,
  CN.LONGITUDE,
  CN.G3E_FID,
  CN.LOCATION,
  CN.LATITUDE,
  CN.STRUCTURE_ID,
  GAS.G3E_GEOMETRY,
  CUN.VINTAGE_YR,
  STN.LUMIN_STYL_C,
  STN.LAMP_TYPE_C,
  STN.WATT_Q
FROM GIS.COMMON_N CN
INNER JOIN GIS.STREETLIGHT_N STN
ON CN.OWNER1_ID = STN.G3E_FID
LEFT JOIN GIS.COMP_UNIT_N CUN
ON CN.G3E_FID = CUN.G3E_FID
INNER JOIN GIS.GUARDAREA_S GAS
ON CN.G3E_FID = GAS.G3E_FID;