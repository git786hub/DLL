  CREATE OR REPLACE VIEW "GIS_ONC"."STREETLIGHT_VW" ("OGGY_H", "OGGX_H", "OWNER1_ID", "FEATURE_STATE_C", "G3E_FNO", "LONGITUDE", "G3E_FID", "LOCATION", "LATITUDE", "STRUCTURE_ID", "PROP_UNIT_ID", "UNIT_CID", "MACRO_CU_C", "WM_SEQ", "CU_C", "PRIME_ACCT_ID", "MATERIAL_C", "HEIGHT_Q", "CLASS_C", "VINTAGE_YR", "LUMIN_STYL_C", "LAMP_TYPE_C", "CONNECT_D", "RATE_SCHEDULE_C", "ACCOUNT_ID", "RATE_CODE_C", "WATT_Q", "G3E_GEOMETRY") AS 
  SELECT CN.OGGY_H,
  CN.OGGX_H,
  CN.OWNER1_ID,
  CN.FEATURE_STATE_C,
  CN.G3E_FNO,
  CN.LONGITUDE, 
  CN.G3E_FID,   
  CN.LOCATION,
  CN.LATITUDE,
  CN.STRUCTURE_ID,
  CUN.PROP_UNIT_ID,
  CUN.UNIT_CID,
  CUN.MACRO_CU_C,
  CUN.WM_SEQ,
  CUN.CU_C,
  CUN.PRIME_ACCT_ID,
  SPL.MATERIAL_C,
  SPL.HEIGHT_Q,
  SPL.CLASS_C,
  CUN.VINTAGE_YR,
  STN.LUMIN_STYL_C,
  STN.LAMP_TYPE_C,
  STN.CONNECT_D,
  STN.RATE_SCHEDULE_C,
  STN.ACCOUNT_ID,
  STN.RATE_CODE_C,
  STN.WATT_Q,
  STS.G3E_GEOMETRY
FROM GIS.COMMON_N CN
INNER JOIN GIS.STREETLIGHT_S STS
ON CN.G3E_FID = STS.G3E_FID
INNER JOIN GIS.STREETLIGHT_N STN
ON CN.G3E_FID = STN.G3E_FID
INNER JOIN GIS.COMP_UNIT_N CUN
ON CN.G3E_FID = CUN.G3E_FID
INNER JOIN STL_POLES_VW SPL
ON CN.G3E_FID = SPL.G3E_FID;
