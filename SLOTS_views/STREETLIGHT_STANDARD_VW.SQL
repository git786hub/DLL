CREATE OR REPLACE VIEW GIS_ONC.STREETLIGHT_STANDARD_VW AS
SELECT CN.OGGY_H,
  CN.OGGX_H,
  CN.OWNER1_ID,
  CN.G3E_FNO,
  CN.FEATURE_STATE_C,
  CN.LONGITUDE,
  CN.G3E_FID,
  CUN.WR_EDITED,
  CUN.WR_ID,
  CN.LOCATION,
  CN.LATITUDE,
  CN.STRUCTURE_ID,
  CUN.VINTAGE_YR,
  STSN.TYPE_C,
  STSN.MATERIAL_C,
  STSN.HEIGHT_Q,
  STSS.G3E_GEOMETRY
FROM GIS.COMMON_N CN
INNER JOIN GIS.STREETLIGHT_STD_N STSN
ON CN.G3E_FID = STSN.G3E_FID
INNER JOIN GIS.STREETLIGHT_STD_S STSS
ON CN.G3E_FID = STSS.G3E_FID
INNER JOIN GIS.COMP_UNIT_N CUN
ON CN.G3E_FID = CUN.G3E_FID ;
