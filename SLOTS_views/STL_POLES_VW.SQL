  CREATE OR REPLACE VIEW "GIS_ONC"."STL_POLES_VW" ("MATERIAL_C", "HEIGHT_Q", "CLASS_C", "G3E_FID") AS 
  SELECT PN.MATERIAL_C,
PN.HEIGHT_Q,
PN.CLASS_C,
SN.G3E_FID
FROM COMMON_N CN
INNER JOIN POLE_N PN
ON CN.G3E_FID = PN.G3E_FID
INNER JOIN COMMON_N SN
ON CN.STRUCTURE_ID = SN.STRUCTURE_ID
WHERE SN.G3E_FNO IN (58,56) AND CN.G3E_FNO = 110;
