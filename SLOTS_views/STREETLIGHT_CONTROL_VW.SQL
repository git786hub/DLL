  CREATE OR REPLACE VIEW "GIS_ONC"."STREETLIGHT_CONTROL_VW" ("OGGY_H", "OGGX_H", "OWNER1_ID", "FEATURE_STATE_C", "G3E_FNO", "LONGITUDE", "LOCATION", "LATITUDE", "STRUCTURE_ID", "PROP_UNIT_ID", "UNIT_CID", "MACRO_CU_C", "WM_SEQ", "CU_C", "PRIME_ACCT_ID", "MATERIAL_C", "HEIGHT_Q", "CLASS_C", "WR_EDITED", "WR_ID", "VINTAGE_YR", "G3E_GEOMETRY") AS 
  SELECT CN.OGGY_H,
  CN.OGGX_H,
  CN.OWNER1_ID,
  CN.FEATURE_STATE_C,
  CN.G3E_FNO,
  CN.LONGITUDE,
  CN.LOCATION,
  CN.LATITUDE,
  CN.STRUCTURE_ID,
  CUN.PROP_UNIT_ID,
  CUN.UNIT_CID,
  CUN.MACRO_CU_C,
  CUN.WM_SEQ,
  CUN.CU_C,
  CUN.PRIME_ACCT_ID,
  SPL.MATERIAL_C,
  SPL.HEIGHT_Q,
  SPL.CLASS_C,
  CUN.WR_EDITED,
  CUN.WR_ID,
  CUN.VINTAGE_YR,
  STCS.G3E_GEOMETRY
FROM GIS.COMMON_N CN
INNER JOIN GIS.STREETLIGHT_CTRL_S STCS
ON CN.G3E_FID = STCS.G3E_FID
INNER JOIN GIS.COMP_UNIT_N CUN
ON CN.G3E_FID = CUN.G3E_FID
INNER JOIN GIS.COMP_UNIT_N CUN
ON CN.G3E_FID = CUN.G3E_FID
INNER JOIN STL_POLES_VW SPL
ON CN.G3E_FID = SPL.G3E_FID;
